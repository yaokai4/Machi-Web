export type AuthLocale = "zh" | "ja" | "en";

export const AUTH_LOCALE_KEY = "machi.auth.locale";

export const AUTH_LOCALE_OPTIONS: Array<{ value: AuthLocale; label: string }> = [
  { value: "zh", label: "中文" },
  { value: "ja", label: "日本語" },
  { value: "en", label: "English" },
];

export function normalizeAuthLocale(value?: string | null): AuthLocale {
  const raw = (value || "").toLowerCase();
  if (raw.startsWith("ja")) return "ja";
  if (raw.startsWith("en")) return "en";
  if (raw.startsWith("zh")) return "zh";
  return "zh";
}

export function detectAuthLocale(): AuthLocale {
  if (typeof window === "undefined") return "zh";
  try {
    const saved = window.localStorage.getItem(AUTH_LOCALE_KEY);
    if (saved) return normalizeAuthLocale(saved);
  } catch {
    // ignore
  }
  const langs = typeof navigator !== "undefined" ? navigator.languages?.length ? navigator.languages : [navigator.language] : [];
  for (const lang of langs) {
    const resolved = normalizeAuthLocale(lang);
    if (resolved !== "zh" || lang.toLowerCase().startsWith("zh")) return resolved;
  }
  return "zh";
}

