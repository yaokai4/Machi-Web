const TABLE: Record<Locale, Partial<Dict>> = {
  "zh-Hans": ZH_HANS,
  "zh-Hant": ZH_HANT,
  en: EN,
  ja: JA,
};

interface I18nContext {
  locale: Locale;
  t: (key: DictKey) => string;
}

const Ctx = createContext<I18nContext>({ locale: "zh-Hans", t: (k) => ZH_HANS[k] });

function normalizeLocale(raw?: string | null): Locale | null {
  const value = (raw || "").trim();
  if (value === "zh-Hant" || value.toLowerCase().startsWith("zh-hant") || value.toLowerCase().includes("traditional")) return "zh-Hant";
  if (value === "zh-Hans" || value === "zh" || value.toLowerCase().startsWith("zh")) return "zh-Hans";
  if (value.toLowerCase().startsWith("ja")) return "ja";
  if (value.toLowerCase().startsWith("en")) return "en";
  return null;
}

export function appLocaleToMarketingLocale(locale: Locale): "zh" | "en" | "ja" {
  if (locale === "en") return "en";
  if (locale === "ja") return "ja";
  return "zh";
}

export function appLocaleToGuideLanguage(locale: Locale): "zh-CN" | "en" | "ja" {
  if (locale === "en") return "en";
  if (locale === "ja") return "ja";
  return "zh-CN";
}

function systemLocale(): Locale {
  if (typeof navigator === "undefined") return "zh-Hans";
  const languages = navigator.languages?.length ? navigator.languages : [navigator.language];
  for (const lang of languages) {
    const normalized = normalizeLocale(lang);
    if (normalized) return normalized;
  }
  return "zh-Hans";
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const settings = useSettings((s) => s.settings);
  const [detectedLocale, setDetectedLocale] = useState<Locale>("zh-Hans");

  useEffect(() => {
    setDetectedLocale(systemLocale());
  }, []);

  const locale: Locale = normalizeLocale(settings?.language) ?? detectedLocale;

  useEffect(() => {
    if (typeof document !== "undefined") {
      if (document.querySelector(".machi-site")) return;
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const value = useMemo<I18nContext>(() => {
    const dict = TABLE[locale];
    const fallback = TABLE["zh-Hans"] as Dict;
    return {
      locale,
      t: (key) => (dict[key] as string | undefined) ?? fallback[key] ?? String(key),
    };
  }, [locale]);

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useI18n() {
  return useContext(Ctx);
}

export type I18nKey = DictKey;
