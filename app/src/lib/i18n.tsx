const TABLE: Record<Locale, Partial<Dict>> = {
  "zh-Hans": ZH_HANS,
  "zh-Hant": ZH_HANT,
  en: EN,
  ja: JA,
};

interface I18nContext {
  locale: Locale;
  t: (key: DictKey) => string;
}

const Ctx = createContext<I18nContext>({ locale: "zh-Hans", t: (k) => ZH_HANS[k] });

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const settings = useSettings((s) => s.settings);
  const locale: Locale = (() => {
    const raw = settings?.language;
    if (raw === "zh-Hans" || raw === "zh-Hant" || raw === "en" || raw === "ja") return raw;
    return "zh-Hans";
  })();

  useEffect(() => {
    if (typeof document !== "undefined") {
      if (document.querySelector(".machi-site")) return;
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const value = useMemo<I18nContext>(() => {
    const dict = TABLE[locale];
    const fallback = TABLE["zh-Hans"] as Dict;
    return {
      locale,
      t: (key) => (dict[key] as string | undefined) ?? fallback[key] ?? String(key),
    };
  }, [locale]);

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useI18n() {
  return useContext(Ctx);
}

export type I18nKey = DictKey;
